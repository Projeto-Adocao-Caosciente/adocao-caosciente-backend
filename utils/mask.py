class Mask:
    # Ex: +55 (11) 99999-9999 | 11999999999 | (11) 99999-9999 | 011 99999 9999
    PHONE = r"^((\+\d{2}\s)?\(?\d{2,3}\)? \d{4,5}[\- ]\d{4}|\d{10,12})$"
    # Ex: 12.345.678/9012-34 | 12345678901234
    CNPJ = r"^(\d{2}\.\d{3}\.\d{3}\/\d{4}\-\d{2}|\d{14})$"
    # Ex: 123.456.789-01 | 12345678901
    CPF = r"^(\d{3}\.\d{3}\.\d{3}\-\d{2}|\d{11})$" 
    # Ex: 2021-10-27
    DATE = r"^\d{4}-\d{2}-\d{2}$"
    # Ex: 12.345-678 | 12345678
    CEP = r"^(\d{5}-\d{3}|\d{8})$"
    